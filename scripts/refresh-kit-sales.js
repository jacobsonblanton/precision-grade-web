/**
 * Refreshes lib/kitSales.ts from the LBX SQL Server database.
 * Run with: node scripts/refresh-kit-sales.js
 *
 * Requires: msnodesqlv8 (already installed)
 * Uses Windows Integrated Authentication — no credentials needed.
 */

const sql = require("../node_modules/msnodesqlv8");
const fs = require("fs");
const path = require("path");

const CONNECTION_STRING =
  "Driver={ODBC Driver 17 for SQL Server};" +
  "Server=lbx-sqlintel;" +
  "Database=LBX_MARKET_Intelligence;" +
  "Trusted_Connection=yes;";

const QUERY = `
  SELECT
    [Branch]             AS branch,
    [Name]               AS name,
    [Invoice]            AS invoice,
    [Net]                AS net,
    [Cost]               AS cost,
    [Qty]                AS qty,
    [Part #]             AS partNo,
    [Description]        AS description,
    [Order Date]         AS orderDate,
    [Order #]            AS orderNo,
    [Service Item Group] AS [group]
  FROM parts_purchases_pcopher
  WHERE [Order Date] >= '2016-01-01'
    AND [Item Category Code] = 'TRIMBLE'
  ORDER BY [Order Date] DESC
`;

console.log("Connecting to lbx-sqlintel...");

sql.query(CONNECTION_STRING, QUERY, (err, rows) => {
  if (err) {
    console.error("Database error:", err.message);
    process.exit(1);
  }

  console.log(`Fetched ${rows.length} rows.`);

  const data = rows.map((r) => ({
    branch: String(r.branch ?? ""),
    name: String(r.name ?? ""),
    invoice: String(r.invoice ?? ""),
    net: Number(r.net ?? 0),
    cost: Number(r.cost ?? 0),
    qty: Number(r.qty ?? 1),
    partNo: String(r.partNo ?? ""),
    description: String(r.description ?? ""),
    orderDate:
      r.orderDate instanceof Date
        ? r.orderDate.toISOString().slice(0, 10)
        : String(r.orderDate ?? ""),
    orderNo: String(r.orderNo ?? ""),
    group: String(r.group ?? ""),
  }));

  const now = new Date().toISOString();
  const output = `// Auto-generated by scripts/refresh-kit-sales.js
// Last refreshed: ${now}
// Source: lbx-sqlintel / LBX_MARKET_Intelligence / parts_purchases_pcopher

export interface KitSale {
  branch: string;
  name: string;
  invoice: string;
  net: number;
  cost: number;
  qty: number;
  partNo: string;
  description: string;
  orderDate: string; // YYYY-MM-DD
  orderNo: string;
  group: string;
}

export const kitSales: KitSale[] = ${JSON.stringify(data, null, 2)};
`;

  const outPath = path.join(__dirname, "..", "lib", "kitSales.ts");
  fs.writeFileSync(outPath, output, "utf8");
  console.log(`Written ${data.length} records to lib/kitSales.ts`);
});
